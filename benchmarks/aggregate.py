"""Aggregate per-run benchmark JSONs into ranking tables (RESULTS.md).

Reads benchmarks/results/<task>/<algorithm>/<dataset>__seed<N>.json, averages
metrics across seeds, ranks algorithms per dataset on the task's primary
metric, and reports average rank per algorithm (raw metric values are never
averaged across datasets — scales differ).

Usage:
    python3 benchmarks/aggregate.py                    # writes RESULTS.md
    python3 benchmarks/aggregate.py --results-dir ... --out RESULTS.md
"""

import argparse
import json
import statistics
from collections import defaultdict
from pathlib import Path

from common import DEFAULT_RESULTS_ROOT, REPO_ROOT, TASKS

# metric used for ranking, and whether higher is better
PRIMARY_METRIC = {
    "classification": ("kappa_t", True),
    "regression": ("rmse", False),
    "clustering": ("ari_mean", True),
    "anomaly_detection": ("auc", True),
}


def load_runs(results_dir):
    """{task: {(algorithm, dataset): [run_record, ...]}} from ok result files."""
    runs = defaultdict(lambda: defaultdict(list))
    for path in sorted(Path(results_dir).rglob("*.json")):
        if path.name.endswith(".failed.json"):
            continue
        record = json.loads(path.read_text())
        if record.get("status") != "ok":
            continue
        runs[record["task"]][(record["algorithm"], record["dataset"])].append(record)
    return runs


def seed_mean(records, metric):
    values = [r["metrics"].get(metric) for r in records]
    values = [v for v in values if v is not None]
    return statistics.mean(values) if values else None


def rank_table(task_runs, metric, higher_better):
    """Per-dataset ranks on the seed-averaged primary metric.

    Returns (per_algorithm_summary, per_dataset_scores).
    """
    scores = defaultdict(dict)  # {dataset: {algorithm: value}}
    wallclock = defaultdict(list)
    for (algorithm, dataset), records in task_runs.items():
        value = seed_mean(records, metric)
        if value is not None:
            scores[dataset][algorithm] = value
        wallclock[algorithm].extend(
            r["wallclock_s"] for r in records if r.get("wallclock_s") is not None
        )

    ranks = defaultdict(list)
    for dataset, by_algo in scores.items():
        ordered = sorted(by_algo, key=by_algo.get, reverse=higher_better)
        for position, algorithm in enumerate(ordered, start=1):
            ranks[algorithm].append(position)

    summary = []
    for algorithm, algo_ranks in ranks.items():
        summary.append(
            {
                "algorithm": algorithm,
                "avg_rank": statistics.mean(algo_ranks),
                "n_datasets": len(algo_ranks),
                "total_wallclock_h": sum(wallclock[algorithm]) / 3600,
            }
        )
    summary.sort(key=lambda row: row["avg_rank"])
    return summary, scores


def fmt(value, digits=3):
    return "—" if value is None else f"{value:.{digits}f}"


def write_markdown(runs, out_path):
    lines = [
        "# StreamArena Baseline Results",
        "",
        "Generated by `benchmarks/aggregate.py` from `benchmarks/results/`. "
        "Protocol: see [BENCHMARK.md](BENCHMARK.md). Algorithms are ranked per "
        "dataset on the task's primary metric and ordered by average rank.",
        "",
    ]
    for task in TASKS:
        if task not in runs:
            continue
        metric, higher_better = PRIMARY_METRIC[task]
        summary, scores = rank_table(runs[task], metric, higher_better)
        direction = "higher is better" if higher_better else "lower is better"
        lines += [
            f"## {task.replace('_', ' ').title()}",
            "",
            f"Primary metric: **{metric}** ({direction}). "
            f"{len(scores)} datasets, {len(summary)} algorithms.",
            "",
            "| Rank | Algorithm | Avg rank | #Datasets | Total wallclock (h) |",
            "|---:|---|---:|---:|---:|",
        ]
        for position, row in enumerate(summary, start=1):
            lines.append(
                f"| {position} | {row['algorithm']} | {row['avg_rank']:.2f} "
                f"| {row['n_datasets']} | {row['total_wallclock_h']:.2f} |"
            )
        lines += ["", f"<details><summary>Per-dataset {metric}</summary>", ""]
        algorithms = [row["algorithm"] for row in summary]
        lines.append("| Dataset | " + " | ".join(algorithms) + " |")
        lines.append("|---|" + "---:|" * len(algorithms))
        for dataset in sorted(scores):
            cells = [fmt(scores[dataset].get(a)) for a in algorithms]
            lines.append(f"| {dataset} | " + " | ".join(cells) + " |")
        lines += ["", "</details>", ""]
    out_path.write_text("\n".join(lines))
    print(f"Wrote {out_path}")


def main():
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--results-dir", type=Path, default=DEFAULT_RESULTS_ROOT)
    parser.add_argument("--out", type=Path, default=REPO_ROOT / "RESULTS.md")
    args = parser.parse_args()

    runs = load_runs(args.results_dir)
    if not runs:
        raise SystemExit(f"No ok result files under {args.results_dir}")
    for task in runs:
        n_failed = len(list(Path(args.results_dir, task).rglob("*.failed.json")))
        if n_failed:
            print(f"note: {task} has {n_failed} failed runs (excluded)")
    write_markdown(runs, args.out)


if __name__ == "__main__":
    main()
