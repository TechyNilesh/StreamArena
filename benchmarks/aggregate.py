"""Aggregate per-run benchmark JSONs into ranking tables (LEADERBOARD.md).

Reads benchmarks/results/<task>/<algorithm>/<dataset>__seed<N>.json, averages
metrics across seeds, ranks algorithms per dataset on the task's primary
metric, and reports average rank per algorithm (raw metric values are never
averaged across datasets — scales differ).

Usage:
    python3 benchmarks/aggregate.py                    # writes LEADERBOARD.md
    python3 benchmarks/aggregate.py --results-dir ... --out LEADERBOARD.md
"""

import argparse
import json
import statistics
from collections import defaultdict
from pathlib import Path

from common import DEFAULT_RESULTS_ROOT, REPO_ROOT, TASKS

# Per task: metric used for per-dataset ranking (higher_better), plus the
# scale-safe display aggregates shown on the leaderboard. Raw kappa_t and RMSE
# must never be averaged across datasets (unbounded / scale-dependent), so
# classification shows median kappa_t + mean accuracy and regression shows
# median R². Within a dataset, kappa_t ranks identically to accuracy (fixed
# NoChange reference) and R² identically to RMSE.
TASK_SPEC = {
    "classification": {
        "rank_on": ("kappa_t", True),
        "display": [("kappa_t", "median", "Median κt"), ("accuracy", "mean", "Mean acc")],
    },
    "regression": {
        "rank_on": ("rmse", False),
        "display": [("r2", "median", "Median R²")],
    },
    "clustering": {
        "rank_on": ("ari_mean", True),
        "display": [("ari_mean", "mean", "Mean ARI")],
    },
    "anomaly_detection": {
        "rank_on": ("auc", True),
        "display": [("auc", "mean", "Mean AUC")],
    },
}


def load_runs(results_dir):
    """{task: {(algorithm, dataset): [run_record, ...]}} from ok result files."""
    runs = defaultdict(lambda: defaultdict(list))
    for path in sorted(Path(results_dir).rglob("*.json")):
        if path.name.endswith(".failed.json"):
            continue
        record = json.loads(path.read_text())
        if record.get("status") != "ok":
            continue
        runs[record["task"]][(record["algorithm"], record["dataset"])].append(record)
    return runs


def seed_mean(records, metric):
    values = [r["metrics"].get(metric) for r in records]
    values = [v for v in values if v is not None]
    return statistics.mean(values) if values else None


def rank_table(task_runs, spec):
    """Per-dataset ranks on the seed-averaged ranking metric, plus wins and
    scale-safe display aggregates.

    Returns (per_algorithm_summary, per_dataset_scores).
    """
    metric, higher_better = spec["rank_on"]
    scores = defaultdict(dict)  # {dataset: {algorithm: value}} for the rank metric
    display_values = defaultdict(lambda: defaultdict(list))  # {algo: {display_metric: [values]}}
    wallclock = defaultdict(list)
    for (algorithm, dataset), records in task_runs.items():
        value = seed_mean(records, metric)
        if value is not None:
            scores[dataset][algorithm] = value
        for dmetric, _, _ in spec["display"]:
            dvalue = seed_mean(records, dmetric)
            if dvalue is not None:
                display_values[algorithm][dmetric].append(dvalue)
        wallclock[algorithm].extend(
            r["wallclock_s"] for r in records if r.get("wallclock_s") is not None
        )

    ranks = defaultdict(list)
    wins = defaultdict(int)
    for dataset, by_algo in scores.items():
        ordered = sorted(by_algo, key=by_algo.get, reverse=higher_better)
        for position, algorithm in enumerate(ordered, start=1):
            ranks[algorithm].append(position)
        wins[ordered[0]] += 1

    summary = []
    for algorithm, algo_ranks in ranks.items():
        row = {
            "algorithm": algorithm,
            "avg_rank": statistics.mean(algo_ranks),
            "wins": wins[algorithm],
            "n_datasets": len(algo_ranks),
            "total_wallclock_h": sum(wallclock[algorithm]) / 3600,
        }
        for dmetric, agg, _ in spec["display"]:
            values = display_values[algorithm][dmetric]
            fn = statistics.median if agg == "median" else statistics.mean
            row[f"display_{dmetric}"] = fn(values) if values else None
        summary.append(row)
    summary.sort(key=lambda row: row["avg_rank"])
    return summary, scores


def fmt(value, digits=3):
    return "—" if value is None else f"{value:.{digits}f}"


def write_markdown(runs, out_path):
    lines = [
        "# StreamArena Baseline Leaderboard",
        "",
        "Generated by `benchmarks/aggregate.py` from `benchmarks/results/`. "
        "Evaluation protocol (prequential test-then-train, seeds, default "
        "hyperparameters): see [BENCHMARK.md](BENCHMARK.md).",
        "",
        "## How to read this leaderboard",
        "",
        "- **Avg rank** — the leaderboard ordering. On every dataset the "
        "algorithms are ranked 1st, 2nd, 3rd, … on that task's primary metric "
        "(seed-averaged), and those per-dataset ranks are averaged. Lower is "
        "better; 1.00 would mean first place on every dataset. We rank instead "
        "of averaging raw metric values because raw values are not comparable "
        "across datasets (different scales, class balances, and difficulty).",
        "- **Wins** — number of datasets where the algorithm ranked 1st.",
        "- **Median κt (kappa-temporal)** — classification skill relative to "
        "the NoChange baseline that always predicts the previous label: "
        "100 is perfect, 0 means no better than NoChange, negative means "
        "worse. On temporally correlated streams plain accuracy is inflated, "
        "so κt is the honest streaming metric. Median (not mean) because κt "
        "is unbounded below on near-constant streams.",
        "- **Mean acc** — plain prequential accuracy, for intuition only.",
        "- **Median R²** — regression fit quality, scale-free across datasets "
        "(1 = perfect, 0 = no better than predicting the mean).",
        "- **Mean ARI** — clustering agreement with ground-truth labels, "
        "averaged over evaluation windows (1 = perfect, 0 = random).",
        "- **Mean AUC** — anomaly-detection ROC-AUC over the full stream of "
        "scores (1 = perfect, 0.5 = random).",
        "- **#Datasets** — coverage; algorithms with documented coverage caps "
        "(see BENCHMARK.md) run on fewer datasets and their avg rank is "
        "computed only where they ran.",
        "- **Total wallclock (h)** — total compute across all runs (all "
        "datasets and seeds). Read it against quality: a small rank gap for a "
        "100× time gap matters in streaming settings.",
        "",
    ]
    for task in TASKS:
        if task not in runs:
            continue
        spec = TASK_SPEC[task]
        metric, higher_better = spec["rank_on"]
        summary, scores = rank_table(runs[task], spec)
        direction = "higher is better" if higher_better else "lower is better"
        display_headers = [label for _, _, label in spec["display"]]
        lines += [
            f"## {task.replace('_', ' ').title()}",
            "",
            f"Ranked per dataset on **{metric}** ({direction}). "
            f"{len(scores)} datasets, {len(summary)} algorithms.",
            "",
            "| Rank | Algorithm | Avg rank | Wins | "
            + " | ".join(display_headers)
            + " | #Datasets | Total wallclock (h) |",
            "|---:|---|---:|---:|" + "---:|" * len(display_headers) + "---:|---:|",
        ]
        for position, row in enumerate(summary, start=1):
            display_cells = " | ".join(
                fmt(row[f"display_{dmetric}"], 2) for dmetric, _, _ in spec["display"]
            )
            lines.append(
                f"| {position} | {row['algorithm']} | {row['avg_rank']:.2f} "
                f"| {row['wins']} | {display_cells} "
                f"| {row['n_datasets']} | {row['total_wallclock_h']:.2f} |"
            )
        lines += ["", f"<details><summary>Per-dataset {metric}</summary>", ""]
        algorithms = [row["algorithm"] for row in summary]
        lines.append("| Dataset | " + " | ".join(algorithms) + " |")
        lines.append("|---|" + "---:|" * len(algorithms))
        for dataset in sorted(scores):
            cells = [fmt(scores[dataset].get(a)) for a in algorithms]
            lines.append(f"| {dataset} | " + " | ".join(cells) + " |")
        lines += ["", "</details>", ""]
    out_path.write_text("\n".join(lines))
    print(f"Wrote {out_path}")


def main():
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--results-dir", type=Path, default=DEFAULT_RESULTS_ROOT)
    parser.add_argument("--out", type=Path, default=REPO_ROOT / "LEADERBOARD.md")
    args = parser.parse_args()

    runs = load_runs(args.results_dir)
    if not runs:
        raise SystemExit(f"No ok result files under {args.results_dir}")
    for task in runs:
        n_failed = len(list(Path(args.results_dir, task).rglob("*.failed.json")))
        if n_failed:
            print(f"note: {task} has {n_failed} failed runs (excluded)")
    write_markdown(runs, args.out)


if __name__ == "__main__":
    main()
